    private void registrotelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_registrotelefonoKeyTyped
    if (registrotelefono.getText().length()== 8){
    evt.consume();
    Toolkit.getDefaultToolkit().beep();
    } 
    if(!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar()))
    {
     Toolkit.getDefaultToolkit().beep();
     JOptionPane.showMessageDialog(this, "Ingrese solo numeros");
     evt.consume();
        }        // Evento para entrar solo numeros
    }//GEN-LAST:event_registrotelefonoKeyTyped
    
    private void registrorutKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_registrorutKeyTyped
    String text=registrorut.getText();
    if (text.length()== 9){
    evt.consume();
    Toolkit.getDefaultToolkit().beep();
    }    
    char car = evt.getKeyChar();
    if(!Character.isDigit(evt.getKeyChar()) && car!='k')
    {
     Toolkit.getDefaultToolkit().beep();
     JOptionPane.showMessageDialog(this, "Ingrese solo numeros");
     evt.consume();
        }        // Evento para entrar solo numeros       // TODO add your handling code here:
    }//GEN-LAST:event_registrorutKeyTyped

