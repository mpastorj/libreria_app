    private void botonregistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonregistroActionPerformed
        // TODO add your handling code here:
        conexion cn=new conexion();
        
        if(registronombre.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "El campo de texto 'Nombre' está vacío");
            }
        String nombre=registronombre.getText();
        if(registrorut.getText().trim().length()==0 | registrorut.getText().trim().length()<8 |
                registrorut.getText().trim().length()>9){
            JOptionPane.showMessageDialog(this, "El campo de texto 'Rut' está incorrecto");
           }
        String rut=registrorut.getText();
        if(registrodireccion.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "El campo de texto 'Dirección' está vacío");}
        String direccion=registrodireccion.getText();
        if(registrotelefono.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "El campo de texto 'Teléfono' está vacío");}
        String telefono=registrotelefono.getText();
        int numero = Integer.parseInt(telefono);// convertir a entero
        if(registrousuario.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "El campo de texto 'Usuario está vacío");}
        String usuario=registrousuario.getText();
        if(registropass.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "El campo de texto 'Pass' está vacío");}
        String pass=registropass.getText();
        try{ 
        if (nombre.length()>0 && rut.length()>=8 && direccion.length()>0 && telefono.length()>0 && usuario.length()>0){
            cn.setEsSelect(false);
                cn.setComandoSQL("insert into usuario (rut, nombre, direccion, telefono, usuarioingreso, contraseña) values('"+rut+"', '"+nombre+"', '"+direccion+"','"+numero+"','"+usuario+"','"+pass+"')");
                cn.conectar();                                                                                                                                                                                                                                                                                                                                                                                                                                                                                              
                cn.cerrarConexion();
                JOptionPane.showMessageDialog(this, "Los datos fueron ingresados correctamente");
                VentanaPrincipal v=new VentanaPrincipal();
                this.dispose();
                }
            }     
        catch(HeadlessException e){
               System.out.println("Se ha producido un error " + e );
        }
    }//GEN-LAST:event_botonregistroActionPerformed

